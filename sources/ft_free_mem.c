#include "../libft/libft.h"
#include "../includes/push_swap.h"

int	ft_free(t_stack *a)
{
	while (a->lenght > 0)
	{
		free(a->numbers[a->lenght]);
		a->lenght--;
	}
	free(a->numbers);
	return (0);
}
